package couse;

public class TiposPrimitivos {
	
	public static void main (String[] args) {
		
		byte vByte = 10;
		boolean vBoolean = false;
		char vChar = 'V';
		char charComUnicode = '\u0041';
		int vInt = 1000;
		long vLong = 8329120810981L;
		float vFloat = 4.5f;
		double vDouble = 4.5;
		
		System.out.println("vByte: " + vByte);
		System.out.println("vBoolean: " + vBoolean);
		System.out.println("vChar: " + vChar);
		System.out.println("charComUnicode: " + charComUnicode);
		System.out.println("vInt: " + vInt);
		System.out.println("vLong: " + vLong);
		System.out.println("vFloat: " + vFloat);
		System.out.println("vDouble: " + vDouble);
		
	}

}
